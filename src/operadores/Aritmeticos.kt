package operadores

fun main() {

    val a = 2
    val b = 5

    // Soma
    println(a + b)

    // Subtração
    println(a - b)

    // Multiplicação
    println(a * b)

    // Divisão
    println(a / b.toDouble())

    // Modulo
    println(a % b)

    // Concatenação
    val c = "Teste" + "Olá"
    println(c)
}