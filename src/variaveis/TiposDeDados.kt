package variaveis

fun main() {
    // Tipos num√©ricos
    /*
    * Byte - 8
    * Short - 16
    * Int - 32
    * Long - 64
    * Float - 32
    * Double - 64
    */

    val byte: Byte = 8
    val short: Short = 360
    val int: Int = 1000
    val long: Long = 10000L
    val float: Float = 150.10F
    val double: Double = 300.10


    println(byte)
    println(short)
    println(int)
    println(long)
    println(float)
    println(double)

    // Tipo Boolean
    // true or false

    val boolean: Boolean = true

    println(boolean)

    // Tipos textuais
    /*
    * Char
    * String
    * */


    val char: Char = 'a'
    val string: String = "Hello World"

    println(string)
    println(char)
}